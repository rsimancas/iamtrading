        private CurrRate Get(int id, SqlConnection oConn)
        {
            string sql = "SELECT a.* FROM CurrencyRates a " +
                         " WHERE (a.CurrencyId = @id)";

            SqlDataAdapter da = new SqlDataAdapter(sql, oConn);

            da.SelectCommand.Parameters.Add("@id", SqlDbType.Int).Value = Convert.ToInt32(id);

            DataSet ds = new DataSet();

            da.Fill(ds);

            DataTable dt;
            dt = ds.Tables[0];

            if (dt.Rows.Count > 0)
            {
                IList<CurrRate> data = EnumExtension.ToList<CurrRate>(dt);
                return data.FirstOrDefault<CurrRate>();
            }

            return null;
        }

